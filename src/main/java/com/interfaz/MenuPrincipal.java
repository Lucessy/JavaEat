    private boolean nearEnough(Restaurante rest, Cliente cliente) {
        String cpRestaurante = rest.getDireccion().getCodigoPostal().substring(0, 4);
        String cpCliente = cliente.getDireccion().getCodigoPostal().substring(0, 4);
        return cpRestaurante.equals(cpCliente);
    }

    private ArrayList<Restaurante> ordenarYFiltrarRestaurantes() {
        ArrayList<Restaurante> restaurantesEspecialidad = new ArrayList<>();
        ArrayList<Restaurante> restaurantesCercanos = new ArrayList<>();
        ArrayList<Restaurante> restaurantesOrdenados = new ArrayList<>();
        for (Restaurante rest : restaurantes) {
            if (nearEnough(rest, clienteActual)) {

                restaurantesCercanos.add(rest);
            }
        }

        if (!("".equals(textoBuscador) || "Comida...".equals(textoBuscador))) {
            System.out.println(textoBuscador);
            String[] palabrasClaves = textoBuscador.split(" ");
            ArrayList<Restaurante> restaurantesBuscados = new ArrayList<>();

            for (Restaurante rest : restaurantesCercanos) {
                String[] tituloRestaurante = rest.getNombre().split(" ");
                boolean contienePalabraClave = false;
                for (String palabraClave : palabrasClaves) {
                    for (String nombre : tituloRestaurante) {
                        if (palabraClave.toLowerCase().equals(nombre.toLowerCase())) {
                            contienePalabraClave = true;
                            break;
                        }
                    }
                }
                if(contienePalabraClave){
                    restaurantesBuscados.add(rest);
                }
            }
            restaurantesCercanos = restaurantesBuscados;
        }

        if (especialidad != "Todas") {
            for (Restaurante rest : restaurantesCercanos) {
                if (especialidad.equals(rest.getEspecialidad())) {

                    restaurantesEspecialidad.add(rest);
                }
            }
            restaurantesCercanos = restaurantesEspecialidad;
        }

        if (!clienteParticular && filtroCatering) {
            for (Restaurante rest : restaurantesCercanos) {
                if (rest.isOfreceCatering()) {
                    restaurantesOrdenados.add(rest);
                }
            }

        } else {
            restaurantesOrdenados = restaurantesCercanos;
        }
        for(Restaurante rest : restaurantesOrdenados){
            System.out.println(rest.getNombre());
        }
        MainManager.bubbleSort(tipoOrden, restaurantesOrdenados);
        return restaurantesOrdenados;

    }

