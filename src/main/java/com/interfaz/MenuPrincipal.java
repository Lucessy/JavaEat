    public void generarPaneles(int num) {
        System.out.println("Generando Paneles");
        int resto = num % 3;
        int numeroPaneles;
        if (resto == 0) {
            numeroPaneles = num / 3;
        } else {
            numeroPaneles = (int) num / 3 + 1;
        }
        for (int i = 0; i < numeroPaneles; i++) {
            TemplatePanels panel = new TemplatePanels();
            int n = 3;
            if (i == numeroPaneles - 1 && resto != 0) {
                n = resto;
            }
            for (int j = 0; j < n; j++) {
                TemplateRestaurante template = new TemplateRestaurante();

                panel.add(template);
                listaTemplates.add(template);
            }
            //panel.setBorder(new LineBorder(Color.red));     //Pintamos de rojo para ver lo limites
            PanelParent.add(panel);
        }
        PanelParent.revalidate();
        PanelParent.repaint();
        Scroll.repaint();
    }

    public void rellenarPaneles(ArrayList<Restaurante> listaRestaurantes) {
        System.out.println("Rellenando Paneles");
        int i = 0;
        for (TemplateRestaurante template : listaTemplates) {
            Restaurante rest = listaRestaurantes.get(i);
            template.setTitulo(rest.getNombre());
            template.setInformacion(rest.getCalificacionMedia(), rest.getEspecialidad(), rest.getGastosEnvio());
            template.setTiempo(rest.getTiempoEnvio());
            template.setProduct(rest);
            String path = rest.getNombreFotografia();
            template.setImageFromPath(path);
            //template.setBorder(new LineBorder(Color.blue)); //Pintamos de azul para ver lo limites
            i++;

        }
    }
    
    
