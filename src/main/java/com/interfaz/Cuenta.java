    public void mostrarDatos(){
        Cliente cliente = MainManager.getClienteIniciado();
        if(cliente instanceof ClienteParticular){
            txtDatos.setText("DATOS PERSONALES");
            txtCifDni.setText(((ClienteParticular) cliente).getDni());
            txtWebApellido.setText(((ClienteParticular) cliente).getApellido());
            txtCIF.setText("");
            txtWeb.setText("");
        }else{
            txtDatos.setText("DATOS EMPRESA");
            txtCifDni.setText(((Empresa) cliente).getCif());
            txtWebApellido.setText(((Empresa) cliente).getWeb());
            txtDNI.setText("");
            txtApellido.setText("");
        }
        //Datos Empresa
        txtNombre1.setText(cliente.getNombre());
        txtCorreo1.setText(cliente.getCorreo());
        txtTelefono1.setText(cliente.getTelefono());
        String clave = "";
        int longitud = cliente.getClave().length();
        for(int i = 0; i<longitud; i++){
            clave += "*";
        }
        txtContrasena1.setText(clave);

        //Dirección
        Direccion direccion = cliente.getDireccion();
        txtCalle1.setText(direccion.getCalle());
        txtCodPostal1.setText(direccion.getCodigoPostal());
        txtNumero1.setText(direccion.getNumero());
        txtCiudad1.setText(direccion.getCiudad());
    }
    
    
