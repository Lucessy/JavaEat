    private void rellenarDatos() {
        Cliente cliente = MainManager.getClienteIniciado();
        if (cliente instanceof ClienteParticular) {
            txtDatos.setText("DATOS PERSONALES");
            fCifDni.setText(((ClienteParticular) cliente).getDni());
            fWebApellido.setText(((ClienteParticular) cliente).getApellido());
            txtCIF.setText("");
            txtWeb.setText("");
        } else {
            txtDatos.setText("DATOS EMPRESA");
            fCifDni.setText(((Empresa) cliente).getCif());
            fWebApellido.setText(((Empresa) cliente).getWeb());
            txtDNI.setText("");
            txtApellido.setText("");
        }
        //Datos Empresa
        fNombre.setText(cliente.getNombre());
        fCorreo.setText(cliente.getCorreo());
        fTelefono.setText(cliente.getTelefono());
        fClave.setText("");
        fClaveNueva.setText("");

        //Dirección
        Direccion direccion = cliente.getDireccion();
        fCalle.setText(direccion.getCalle());
        fNum.setText(direccion.getNumero());
        fCodPostal.setText(direccion.getCodigoPostal());
        fCiudad.setText(direccion.getCiudad());
    }


