    private void jLabel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel1MouseClicked
        Cliente cliente = MainManager.getClienteIniciado();
        Cliente clienteTemporal;
        //Datos Empresa
        String nombre = fNombre.getText();
        String correo = fCorreo.getText();
        String telefono = fTelefono.getText();
        String claveActual = String.valueOf(fClave.getPassword());
        String claveNueva = String.valueOf(fClaveNueva.getPassword());

        //Dirección
        String calle = fCalle.getText();
        String numero = fNum.getText();
        String codPostal = fCodPostal.getText();
        String ciudad = fCiudad.getText();
        Direccion direccionTemp = new Direccion(calle, numero, codPostal, ciudad);

        if (cliente instanceof ClienteParticular) {
            String dni = fCifDni.getText();
            String apellido = fWebApellido.getText();
            if (nombre.equals("") || dni.equals("") || correo.equals("") || telefono.equals("")
                    || apellido.equals("") || claveActual.equals("") || calle.equals("") || codPostal.equals("")
                    || numero.equals("") || ciudad.equals("")) {
                JOptionPane.showMessageDialog(this, "Introduzca todos los datos", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (claveActual.equals("")) {
                JOptionPane.showMessageDialog(this, "Para modificar los datos necesita introdcuir su contraseña actual", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            ClienteParticular clienteTemp = new ClienteParticular(dni, nombre, apellido, direccionTemp, telefono, correo, claveActual, MainManager.getClientes().size() + 1);
            clienteTemporal = clienteTemp;

        } else {
            String cif = fCifDni.getText();
            String web = fWebApellido.getText();
            if (nombre.equals("") || cif.equals("") || correo.equals("") || telefono.equals("")
                    || web.equals("") || calle.equals("") || codPostal.equals("")
                    || numero.equals("") || ciudad.equals("")) {
                JOptionPane.showMessageDialog(this, "Introduzca todos los datos", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (claveActual.equals("")) {
                JOptionPane.showMessageDialog(this, "Para modificar los datos necesita introdcuir su contraseña actual", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            Empresa clienteTemp = new Empresa(cif, web, nombre, direccionTemp, telefono, correo, claveActual, MainManager.getClientes().size() + 1);
            clienteTemporal = clienteTemp;
        }

        String mensaje = MainManager.verificaDatos(clienteTemporal, claveNueva);
        if (!mensaje.equals("")) {
            JOptionPane.showMessageDialog(this, mensaje, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jLabel1MouseClicked

