    private void actualizarLista(ArrayList<Restaurante> restaurantes) {
        System.out.println("Actualizando lista...");
        jPanelContenido.removeAll();
        for (Restaurante restaurante : restaurantes) {
            JButton jbutton = new JButton();
            jbutton.setFont(new java.awt.Font("Segoe UI", 0, 15));
            jbutton.setText("Restaurante " + restaurante.getId() + " | " + restaurante.getNombre());

            jbutton.setHorizontalAlignment(javax.swing.SwingConstants.LEFT);
            jbutton.setMaximumSize(new java.awt.Dimension(696, 100));
            jbutton.setMinimumSize(new java.awt.Dimension(696, 100));
            jbutton.addActionListener(new java.awt.event.ActionListener() {
                public void actionPerformed(java.awt.event.ActionEvent evt) {
                    jButtonActionPerformed(restaurante);
                }
            });
            jPanelContenido.add(jbutton);
        }
        jPanelContenido.revalidate();
        jPanelContenido.repaint();
    }

    private void jButtonActionPerformed(Restaurante restaurante) {
        ContenidoRestaurante contenidoRestaurante = new ContenidoRestaurante(restaurante);
        MainManager.showPanel(contenidoRestaurante, 670, 427, jPanelContenido);
        jButtonAnadir.setVisible(false);
        jButtonModificar.setVisible(true);
        restauranteActual = restaurante;

    }

