    private void jButtonAnadirMenu1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAnadirMenu1ActionPerformed
        String comidaElegida = jListComidas.getSelectedValue();
        if(comidaElegida == null){
            JOptionPane.showMessageDialog(this, "Seleccione una comida para eliminar", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
                comidasElegidas.add(comidasTemp.get(jListComidas.getSelectedIndex()));
                comidasTemp.remove(jListComidas.getSelectedIndex());
                updateLista();
                actualizarComidas();
                actualizarPrecio();
        }
    }//GEN-LAST:event_jButtonAnadirMenu1ActionPerformed

