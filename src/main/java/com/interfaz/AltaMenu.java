    private void jButtonAnadirImagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAnadirImagenActionPerformed
        JFileChooser fc = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.jpg", "jpg");
        fc.setFileFilter(filter);
        int selection = fc.showOpenDialog(this);
        if (selection == JFileChooser.APPROVE_OPTION) {
            File file = fc.getSelectedFile();
            BufferedImage image;
            try {
                image = ImageIO.read(file);
            } catch (IOException e) {
                System.out.print("Error IO: " + e.getMessage());
                JOptionPane.showMessageDialog(this, "Elige una imagen válida", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            File outputImage;
            
                outputImage = new File("src/main/resources/otro/RESTAURANTE" + altaRestaurante.getRestaurante().getId() + "/MENU" + altaRestaurante.getAvailableIdMenu() + ".jpg");
                nombreFotografia = "src/main/resources/otro/RESTAURANTE" + altaRestaurante.getRestaurante().getId() + "/MENU" + altaRestaurante.getAvailableIdMenu() + ".jpg";
            
            try {
                boolean write = ImageIO.write(image, "jpg", outputImage);
                System.out.println(write);
            } catch (IOException e) {
                System.out.print("Error IO: " + e.getMessage());
                JOptionPane.showMessageDialog(this, "Elige una imagen válida", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            ImageIcon icon = Restaurante.resizedIcon(jIcon.getBounds().width, jIcon.getBounds().height, file.getAbsolutePath());
            if (icon != null) {
                jIcon.setIcon(icon);
            }
        }
    }//GEN-LAST:event_jButtonAnadirImagenActionPerformed
    
    private void actualizarPrecio() {
        double suma = 0.0;
        for(Comida comida : comidasElegidas){
            suma += comida.getPrecioVenta();
        }
        jPrecio.setText(String.format("%.2f%n", suma*0.95) + " €");
    }

    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        //Datos Empresa
        String nombre = jNombre.getText();
        int listaComidas = jListComidas1.getModel().getSize();

        if (nombre.isEmpty() || listaComidas == 0) {
            JOptionPane.showMessageDialog(this, "Por favor, introduzca todos los datos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (nombreFotografia == null) {
            JOptionPane.showMessageDialog(this, "Por favor, introduzca una imagen", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Menu menu = new Menu(nombre,nombreFotografia,comidasElegidas,altaRestaurante.getAvailableIdMenu());
        
        altaRestaurante.addtoListaMenu(menu);
        this.setVisible(false);
        altaRestaurante.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_jButtonGuardarActionPerformed
    
    public void updateLista() {
        ArrayList<String> lineasVenta = new ArrayList<>();
        for (Comida comida : comidasElegidas) {
            lineasVenta.add(comida.getTitulo() + " (" + comida.getId() + ") " + Double.valueOf(comida.getPrecioVenta()) + "€");
        }
        DefaultListModel<String> model = new DefaultListModel<String>();
        model.addAll(lineasVenta);
        jListComidas1.setModel(model);
        jListComidas1.revalidate();
        jListComidas1.repaint();
    }
    
