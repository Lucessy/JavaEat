    private void correofieldFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_correofieldFocusGained
        if(correofield.getText().equals("Ingrese el correo de usuario")){
            correofield.setText("");
            correofield.setForeground(Color.black);
        }
    }//GEN-LAST:event_correofieldFocusGained

    private void correofieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_correofieldFocusLost
        if(correofield.getText().isEmpty()){
            correofield.setText("Ingrese el correo de usuario");
            correofield.setForeground(new Color(204,204,204));
        }
    }//GEN-LAST:event_correofieldFocusLost

