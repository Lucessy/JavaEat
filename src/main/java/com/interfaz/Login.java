    private void flechaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_flechaMouseClicked
        
        String correo = correofield.getText();
        String clave = String.valueOf(pswfield.getPassword());
        if(correo.equals("Ingrese el correo de usuario") || correo.equals("")){
            JOptionPane.showMessageDialog(this, "Introduzca un correo","Datos incorrectos",JOptionPane.ERROR_MESSAGE);

        } else if (clave.equals("********") || clave.equals("")) {
            JOptionPane.showMessageDialog(this, "Introduzca una contraseña","Datos incorrectos",JOptionPane.ERROR_MESSAGE);

        } else{
            String mensaje = MainManager.verificaLogin(correo, clave);
            if(!mensaje.equals("")){
                JOptionPane.showMessageDialog(this, mensaje, "Datos incorrectos", JOptionPane.ERROR_MESSAGE);
                pswfield.setText("");
            } 
            
        }
             
        
    }//GEN-LAST:event_flechaMouseClicked

